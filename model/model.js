//sends page (name) information to the view (index.html)
export function modelPageName(pgName) {
  // console.log(eval(pgName) != "");
  // $("#app").html(eval(pgName));

  //ADD AND REMOVE CLASSES
  if (pgName == "homeContent") {
    $("nav a").removeClass("other-text").addClass("white-text");
    $("nav").removeClass("nav-black").addClass("nav-white");
  } else {
    $("nav a").removeClass("white-text").addClass("other-text");
    $("nav").removeClass("nav-white").addClass("nav-black");
  }

  //NEW METHOD TO CHECK IF THERE IS A PAGE WITHOUT BREAKING APP
  try {
    eval(pgName);
    $("#app").html(eval(pgName));
  } catch (e) {
    console.log("hello ", e);
  }
}
